#include <iostream>
using namespace std;
int main() {
	int decimalOne = 1;
	cout << "decimalOne value = " << decimalOne << "\n";
	int binaryThree = 0b11;
	cout << "binaryThree value = " << binaryThree << "\n";
	int octalEight = 010;
	cout << "octalEight value = " << octalEight << "\n";
	int hexTen = 0xA;
	cout << "hexTen value = "<< hexTen << "\n";
	int asciiValueOfOne = '1';
	cout << "asciiValueOfOne value = " << asciiValueOfOne << "\n";
	int asciiValueOfA = 'A';
	cout << "asciiValueOfA value = " <<asciiValueOfA << "\n";
	int asciiValueOfZ = 'Z';
	cout << "asciiValueOfZ value = " << asciiValueOfZ << "\n";
}
